                        <h2>History</h2>
                        <p>
                            From the start in 1978, OotM recognized the
                            importance of many of the skills now emphasized
                            in education today such as Science, Technology,
                            Engineering and Math (STEM). The program
                            combines that with history and art and has
                            encouraged learning these subjects through the
                            Classics and performance problems.<br /><br />
                            The program was introduced in Switzerland in
                            2004. Since 2007 and up to 2019, teams in
                            Switzerland participated in OotM EuroFest and
                            since 2009 teams in Switzerland have
                            participated in the World Finals.<br /><br />
                            All OotM solutions require students to perform –
                            whether it be in set-building, costume making,
                            creating props, acting, singing, or playing an
                            instrument. These skills are important to create
                            a balanced education. Because the students are
                            doing something fun and have ownership of their
                            solutions, they are eager to perform. They
                            develop self-confidence and public speaking
                            skills along the way.
                        </p>
                    </article>
                    <article>
                        <h2>Spontaneous</h2>
                        <p>
                            Spontaneous is that part of the competition that
                            team members really get to shine and share those
                            creative thinking skills that coaches get to see
                            through the long-term problem process.<br /><br />
                            They are solved instantly using spontaneous
                            creativity. Teams practice and prepare for the
                            Spontaneous experience but the actual
                            Spontaneous Problem they will solve at a
                            tournament is not revealed to them until they
                            walk into the room to solve the problem!
                            <br /><br />
                            Spontaneous develops quick thinking, creativity,
                            teamwork, and the ability to deal with the
                            unexpected which are all important educational
                            aspects of Odyssey of the Mind.
                        </p>
